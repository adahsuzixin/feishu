type SSHClient struct {
	Host      string
	Port      string
	Username  string
	Passwd    string
	Sshclient *Client
}

func NewSSHClient(host, port, username, passwd string) (*SSHClient, error) {
	addr := fmt.Sprintf("%s:%s", host, port)
	ssh_config := &ssh.ClientConfig{
		User: username,
		Auth: []ssh.AuthMethod{
			ssh.Password(passwd),
		},
		HostKeyCallback: ssh.HostKeyCallback(func(hostname string, remote net.Addr, key ssh.PublicKey) error { return nil }),
		Timeout:         time.Duration(10 * time.Second),
	}

	ssh_client, err := ssh.Dial("tcp", addr, ssh_config)
	if err != nil {
		return nil, err
	}
	sshcli := NewClient(ssh_client)
	if err != nil {
		return nil, err
	}
	if err != nil {
		return nil, err
	}
	return &SSHClient{
		Host:      host,
		Port:      port,
		Username:  username,
		Passwd:    passwd,
		Sshclient: sshcli,
	}, nil
}

type remoteScriptType byte
type remoteShellType byte

const (
	cmdLine remoteScriptType = iota
	rawScript
	scriptFile

	interactiveShell remoteShellType = iota
	nonInteractiveShell
)

type Client struct {
	client *ssh.Client
}

func NewClient(client *ssh.Client) *Client {
	return &Client{
		client: client,
	}
}
func Dial(network, addr string, config *ssh.ClientConfig) (*Client, error) {
	client, err := ssh.Dial(network, addr, config)
	if err != nil {
		return nil, err
	}
	return &Client{
		client: client,
	}, nil
}

func (c *Client) Close() error {
	return c.client.Close()
}

func (c *Client) RunCmd(cmd string) (string, error) {
	script := c.Cmd(cmd)
	err := script.Run()
	if err != nil {
		return "", err
	}
	output, err := script.Output()
	return string(output), err

}

// Cmd create a command on client
func (c *Client) Cmd(cmd string) *remoteScript {
	return &remoteScript{
		_type:  cmdLine,
		client: c.client,
		script: bytes.NewBufferString(cmd + "\n"),
	}
}

func (c *SSHClient) Copy(ctx context.Context, src, dst string) error {
	file, err := os.Open(src)
	if err != nil {
		return err
	}
	scpcli, err := scp.NewClientBySSH(c.Sshclient.client)
	if err != nil {
		return err
	}
	err = scpcli.CopyFromFile(ctx, *file, dst, "0655")
	if err != nil {
	}
	defer file.Close()
	defer scpcli.Close()
	return err
}

func (c *SSHClient) CopyBack(ctx context.Context, src, dst string) error {
	var file *os.File
	var err error
	if !Exists(dst) {
		os.MkdirAll(path.Dir(dst), 0777)
		file, err = os.OpenFile(dst, os.O_CREATE|os.O_WRONLY, 0777)
	} else {
		file, err = os.OpenFile(dst, os.O_WRONLY, 0777)
	}

	if err != nil {
		return err
	}

	scpcli, err := scp.NewClientBySSH(c.Sshclient.client)
	if err != nil {
		return err
	}
	err = scpcli.CopyFromRemote(ctx, file, src)
	if err != nil {
		return err
	}
	defer file.Close()
	defer scpcli.Close()
	return err
}

